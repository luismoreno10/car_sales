    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        int fila = table.getSelectedRow();
        int ID = Integer.valueOf(table.getValueAt(fila, 0).toString());
        Integer car_Sold_ID = (Integer) ht_carSold.get(carSoldComboBox.getSelectedItem());
        Integer customer_ID = (Integer) ht_customerID.get(customerIDComboBox.getSelectedItem());
        Integer payment_Status_Code = (Integer) ht_payment.get(PaymentStatusComboBox.getSelectedItem());
        String due_date = "'"+Due_year.getText()+"-"+Due_Month.getText()+"-"+Due_Day.getText()+"'";
        String made_date = "'"+Made_year1.getText()+"-"+Made_Month.getText()+"-"+made_day.getText()+"'";
        Double payment = Double.parseDouble(actualPayment.getText());

        StringBuilder sql
        = new StringBuilder("UPDATE public.\"Customer_Payments\"\n" +
"	SET  "
            + "\"car_Sold_ID\"="+car_Sold_ID.intValue()+", "
            + "\"customer_ID\"="+customer_ID.intValue()+", "
            + "\"payment_Status_Code\"="+payment_Status_Code.intValue()+", "
            + "\"customer_Payment_Date_Due\"="+due_date+", "
            + "\"customer_Payment_Date_Made\"="+made_date+", "
            + "\"actual_Payment_Amount\"="+payment.doubleValue()+"\n" +
"	WHERE \"customer_Payment_ID\" = "+ID+"");
       
        try {
            BD.update( sql.toString() );
            JOptionPane.showMessageDialog(this, "Updated!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error updating! \n"+ex.toString());
            System.out.println( ex.getMessage() );
        }
        mostrardatos("");
    }//GEN-LAST:event_addButtonActionPerformed

